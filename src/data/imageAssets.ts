// Helper function to get images by category
export const getImagesByCategory = (category: string) => {
  return imageAssets[category as keyof typeof imageAssets] || [];
};

// Helper function to get a random image from a category
export const getRandomImageFromCategory = (category: string) => {
  const images = getImagesByCategory(category);
  return images[Math.floor(Math.random() * images.length)];
};

